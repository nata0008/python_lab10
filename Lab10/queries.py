import psycopg2
from tabulate import tabulate

conn = psycopg2.connect(
    dbname="hr_department", user="admin", password="secretpassword",
    host="localhost", port="5433"
)
cursor = conn.cursor()

def print_query(title, query, params=None):
    print(f"\n{'='*80}\n{title}\n{'-'*80}")
    cursor.execute(query, params)
    rows = cursor.fetchall()
    cols = [desc[0] for desc in cursor.description]
    print(tabulate(rows, headers=cols, tablefmt='grid'))

# 0. Відображення структури і даних однієї з таблиць (наприклад, Відділи)
print_query("Всі таблиці: Дані таблиці 'Відділи'", "SELECT * FROM Відділи;")
print_query("Всі таблиці: Дані таблиці 'Посади'", "SELECT * FROM Посади;")

# 1. Відобразити всіх робітників, які мають оклад більший за 2000 грн. Відсортувати прізвища за алфавітом.
q1 = """
SELECT с.Прізвище, с.Ім_я, п.Посада, п.Оклад 
FROM Співробітники с
JOIN Посади п ON с.Код_посади = п.Код_посади
WHERE п.Оклад > 2000
ORDER BY с.Прізвище ASC;
"""
print_query("1. Робітники з окладом > 2000 грн (відсортовано)", q1)

# 2. Порахувати середню зарплатню в кожному відділі (підсумковий запит).
q2 = """
SELECT в.Назва_відділу, ROUND(AVG(п.Оклад), 2) AS Середня_зарплатня
FROM Співробітники с
JOIN Відділи в ON с.Код_відділу = в.Код_відділу
JOIN Посади п ON с.Код_посади = п.Код_посади
GROUP BY в.Назва_відділу;
"""
print_query("2. Середня зарплатня в кожному відділі", q2)

# 3. Відобразити всі проекти, які виконуються в обраному відділі (запит з параметром).
target_dept = 'програмування'
q3 = """
SELECT пр.Назва_проекту, вп.Дата_початку, в.Назва_відділу
FROM Проекти пр
JOIN Виконання_проектів вп ON пр.Номер_проекту = вп.Номер_проекту
JOIN Відділи в ON вп.Код_відділу = в.Код_відділу
WHERE в.Назва_відділу = %s;
"""
print_query(f"3. Проекти, які виконуються у відділі '{target_dept}'", q3, (target_dept,))

# 4. Порахувати кількість працівників у кожному відділу (підсумковий запит).
q4 = """
SELECT в.Назва_відділу, COUNT(с.Код_співробітника) AS Кількість_співробітників
FROM Відділи в
LEFT JOIN Співробітники с ON в.Код_відділу = с.Код_відділу
GROUP BY в.Назва_відділу;
"""
print_query("4. Кількість працівників у кожному відділі", q4)

# 5. Порахувати розмір премії для кожного співробітника (запит з обчислювальним полем).
q5 = """
SELECT с.Прізвище, с.Ім_я, п.Оклад, п.Премія_відсоток, 
       ROUND((п.Оклад * п.Премія_відсоток / 100), 2) AS Розмір_премії_грн
FROM Співробітники с
JOIN Посади п ON с.Код_посади = п.Код_посади;
"""
print_query("5. Розмір премії для кожного співробітника", q5)

# 6. Порахувати кількість робітників які мають спеціальну, середню, вищу освіту у кожному відділу (перехресний запит).
# Для PostgreSQL використовуємо функцію FILTER для створення перехресного звіту.
q6 = """
SELECT в.Назва_відділу,
       COUNT(с.Код_співробітника) FILTER (WHERE с.Освіта = 'спеціальна') AS Спеціальна,
       COUNT(с.Код_співробітника) FILTER (WHERE с.Освіта = 'середня') AS Середня,
       COUNT(с.Код_співробітника) FILTER (WHERE с.Освіта = 'вища') AS Вища
FROM Відділи в
LEFT JOIN Співробітники с ON в.Код_відділу = с.Код_відділу
GROUP BY в.Назва_відділу;
"""
print_query("6. Кількість робітників за типами освіти у кожному відділі", q6)

